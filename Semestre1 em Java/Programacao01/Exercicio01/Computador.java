public class Computador {
    String marca;
    double velocidade;
    int ano;
    boolean isNovo;
public Computador(){
    this.marca="Volkswagen";
    this.velocidade=200;
    this.ano=2025;
    this.isNovo=true;
}
public Computador(String marca, double velocidade, int ano, boolean isNovo){
    this.marca=marca;
    this.velocidade=velocidade;
    this.ano=ano;
    this.isNovo=isNovo;
}
}

